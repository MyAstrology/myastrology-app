// VSOP87C মঙ্গল হেলিওসেন্ট্রিক দ্রাঘিমাংশ গণনা
function computeMarsHeliocentricL(T) {
    const tau = T / 10;
    let L = 0.0;
    VSOP87_MARS_TERMS.L0.forEach(term => {
        L += term[0] * Math.cos(term[1] + term[2] * tau);
    });
    VSOP87_MARS_TERMS.L1.forEach(term => {
        L += term[0] * Math.cos(term[1] + term[2] * tau) * tau;
    });
    VSOP87_MARS_TERMS.L2.forEach(term => {
        L += term[0] * Math.cos(term[1] + term[2] * tau) * tau * tau;
    });
    VSOP87_MARS_TERMS.L3.forEach(term => {
        L += term[0] * Math.cos(term[1] + term[2] * tau) * tau * tau * tau;
    });
    return L / 1e8; // radians
}

// মঙ্গল হেলিওসেন্ট্রিক ব্যাসার্ধ (R) — VSOP87D সিরিজ (Keplerian আনুমানিকের বদলে)
function computeMarsHeliocentricR(T) {
    const tau = T / 10;
    let R = 0.0;
    VSOP87_MARS_TERMS.R0.forEach(term => {
        R += term[0] * Math.cos(term[1] + term[2] * tau);
    });
    VSOP87_MARS_TERMS.R1.forEach(term => {
        R += term[0] * Math.cos(term[1] + term[2] * tau) * tau;
    });
    VSOP87_MARS_TERMS.R2.forEach(term => {
        R += term[0] * Math.cos(term[1] + term[2] * tau) * tau * tau;
    });
    return R / 1e8; // AU
}

// ────────────────────────────────────────────────────────────────────────────
// মঙ্গল গ্রহের সায়ন (Tropical) দ্রাঘিমাংশ — High Precision
// ────────────────────────────────────────────────────────────────────────────
function marsL_tropical(jd) {
    const T = (jd - 2451545.0) / 36525.0;
    
    // 1. মঙ্গলের হেলিওসেন্ট্রিক দ্রাঘিমাংশ (VSOP87C)
    const marsHelioLon = computeMarsHeliocentricL(T) * DEG;
    const marsHelioR = computeMarsHeliocentricR(T);
    
    // 2. পৃথিবীর হেলিওসেন্ট্রিক অবস্থান (বিদ্যমান _earthHel ফাংশন ব্যবহার)
    const earth = _earthHel(jd);
    
    // 3. জিওসেন্ট্রিক দ্রাঘিমাংশ গণনা (গ্রহ-পৃথিবী জ্যামিতি)
    const geo = _geo(marsHelioLon, marsHelioR, earth.lon, earth.r);
    
    return rev(geo);
}

// ────────────────────────────────────────────────────────────────────────────
// মঙ্গল গ্রহের নিরয়ন (Sidereal) দ্রাঘিমাংশ — মূল ফাংশন
// ────────────────────────────────────────────────────────────────────────────
function marsL(jd) {
    const trop = marsL_tropical(jd);
    const ay = lahiriAY(jd);
    return rev(trop - ay);
}

