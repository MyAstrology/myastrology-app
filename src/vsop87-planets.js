// VSOP87A বুধ হেলিওসেন্ট্রিক দ্রাঘিমাংশ গণনা
function computeMercuryHeliocentricL(T) {
    const tau = T / 10; // VSOP87 চলক: Julian centuries → Julian millennia
    let L = 0.0;

    // L0 টার্ম গণনা (১৪৫ পদ)
    VSOP87_MERCURY_TERMS.L0.forEach(term => {
        L += term[0] * Math.cos(term[1] + term[2] * tau);
    });

    // L1 টার্ম গণনা (৪৫ পদ)
    VSOP87_MERCURY_TERMS.L1.forEach(term => {
        L += term[0] * Math.cos(term[1] + term[2] * tau) * tau;
    });

    // L2 টার্ম গণনা (১০ পদ)
    VSOP87_MERCURY_TERMS.L2.forEach(term => {
        L += term[0] * Math.cos(term[1] + term[2] * tau) * tau * tau;
    });

    // সঠিক একক রূপান্তর: 10^-8 radian → radian (DEG দিয়ে গুণ হবে _geo-তে)
    return L / 1e8;
}

// বুধ হেলিওসেন্ট্রিক অক্ষাংশ (B) — VSOP87 সিরিজ
// বুধের ৭° কক্ষীয় নতির কারণে B উপেক্ষা করলে geocentric lon-এ ~৯' ভুল হয়
function computeMercuryHeliocentricB(T) {
    const tau = T / 10;
    let B = 0.0;
    VSOP87_MERCURY_TERMS.B0.forEach(t => B += t[0] * Math.cos(t[1] + t[2] * tau));
    VSOP87_MERCURY_TERMS.B1.forEach(t => B += t[0] * Math.cos(t[1] + t[2] * tau) * tau);
    VSOP87_MERCURY_TERMS.B2.forEach(t => B += t[0] * Math.cos(t[1] + t[2] * tau) * tau * tau);
    return B / 1e8; // radians
}

// বুধ হেলিওসেন্ট্রিক ব্যাসার্ধ (R) — VSOP87 সম্পূর্ণ সিরিজ
