// লগ্ন গণনা — RAMC পদ্ধতি (Placidus-compatible sidereal ascendant)
// Bishuddha Siddhanta অনুযায়ী নিরয়ণ (Sidereal) লগ্ন
// lat, lon = ডিগ্রিতে (+ উত্তর, + পূর্ব)
// ════════════════════════════════════════════════════════════════════════════
function getLagna(jd, lat, lon) {
  const T   = (jd - 2451545.0) / 36525.0;

  // Greenwich Mean Sidereal Time (GMST) ডিগ্রিতে
  const JD0   = Math.floor(jd - 0.5) + 0.5;
  const UT    = (jd - JD0) * 24.0;
  const T0    = (JD0 - 2451545.0) / 36525.0;
  let   gmst  = 100.46061837
              + 36000.770053608 * T0
              + 0.000387933     * T0 * T0
              + 360.985647366   * (UT / 24.0);
  gmst = rev(gmst);

  // নিউটেশন (Δψ, Δε)
  const omega = rev(125.04452 - 1934.136261 * T) * RAD;
  const Ls    = rev(280.4665  + 36000.7698   * T) * RAD;
  const Lm    = rev(218.3165  + 481267.8813  * T) * RAD;
  const dpsi  = (-17.20*Math.sin(omega) - 1.32*Math.sin(2*Ls)
                 - 0.23*Math.sin(2*Lm)  + 0.21*Math.sin(2*omega)) / 3600.0;
  const deps   = (  9.20*Math.cos(omega) + 0.57*Math.cos(2*Ls)
                 +  0.10*Math.cos(2*Lm)  - 0.09*Math.cos(2*omega)) / 3600.0;

  // প্রকৃত নক্ষত্র সময় (Apparent Sidereal Time)
  const eps0  = 23.4392911 - 46.8150/3600*T - 0.00059/3600*T*T + 0.001813/3600*T*T*T;
  const eps   = (eps0 + deps) * RAD;

  // দ্রাঘিমাংশের জন্য LMST
  let lmst = rev(gmst + lon + dpsi * Math.cos(eps));
  const lmst_r = lmst * RAD;

  // Ascendant (সায়ন)
  const lat_r  = lat * RAD;
  const y      = -Math.cos(lmst_r);
  const x      =  Math.sin(eps) * Math.tan(lat_r) + Math.cos(eps) * Math.sin(lmst_r);
  let   asc    = Math.atan2(y, x) * DEG;
  asc = rev(asc);

  // নিরয়ণ লগ্ন = সায়ন + ১৮০° − অয়নাংশ
  // atan2 formula yields the descending (setting) ecliptic point; +180° gives the ascending point
  return rev(asc + 180 - lahiriAY(jd));
}

// ════════════════════════════════════════════════════════════════════════════
// সূর্যোদয় / সূর্যাস্ত — সূর্যের কেন্দ্রবিন্দু থেকে (Bishuddha Siddhanta)
// Returns { rise: JD, set: JD }
// altitude = 0 (center), refraction ≈ 0.5667° যোগ করে horizon dip দেওয়া হয়েছে
// ════════════════════════════════════════════════════════════════════════════
function _sunApparentRA_Dec(jd) {
  const T  = (jd - 2451545.0) / 36525.0;
  const sunTrop = sunL(jd);  // tropical longitude

  // obliquity
  const eps = (23.4392911 - 46.8150/3600*T) * RAD;
  const lon_r = sunTrop * RAD;

  const ra  = Math.atan2(Math.cos(eps)*Math.sin(lon_r), Math.cos(lon_r)) * DEG;
  const dec = Math.asin(Math.sin(eps)*Math.sin(lon_r)) * DEG;
  return { ra: rev(ra), dec };
}

function getSunriseSunset(y, m, d, lat, lon_deg) {
  // Bishuddha Siddhanta: altitude = -0.5667° (centre, standard refraction+dip)
  const h0 = -0.5667 * RAD;

  function iterateSun(jd_approx) {
    // Transit JD
    const T     = (jd_approx - 2451545.0) / 36525.0;
    const Lsun  = rev(280.46646 + 36000.76983 * T);
    const { ra } = _sunApparentRA_Dec(jd_approx);

    // GMST at 0h UT
    const JD0    = Math.floor(jd_approx - 0.5) + 0.5;
    const T0     = (JD0 - 2451545.0) / 36525.0;
    const gmst0  = rev(100.46061837 + 36000.770053608*T0 + 0.000387933*T0*T0);

    // Hour angle at transit
    let m_transit = (ra - gmst0 - lon_deg) / 360.0;
    m_transit = m_transit - Math.floor(m_transit);

    // Hour angle at rise/set
    const { dec } = _sunApparentRA_Dec(jd_approx);
    const lat_r   = lat * RAD;
    const cosH    = (Math.sin(h0) - Math.sin(lat_r)*Math.sin(dec*RAD))
                  / (Math.cos(lat_r)*Math.cos(dec*RAD));

    if (cosH < -1 || cosH > 1) return null; // polar day/night

    const H  = Math.acos(cosH) * DEG;
    const m0 = m_transit;
    const m1 = m_transit - H / 360.0;
    const m2 = m_transit + H / 360.0;

    const toJD = frac => JD0 + frac;
    return { rise: toJD(m1), set: toJD(m2), transit: toJD(m0) };
  }

  const jd_approx = JD(y, m, d) + 0.5 - lon_deg/360.0;

  // ২ iteration
  let result = iterateSun(jd_approx);
  if (!result) return null;
  result = iterateSun((result.rise + result.set) / 2);

  return result;
}

// JD → HH:MM:SS IST string
function jdToIST(jd) {
  const ut_h  = ((jd + 0.5) % 1) * 24;
  const ist_h = ((ut_h + 5.5) % 24);
  const h  = Math.floor(ist_h);
  const m  = Math.floor((ist_h - h) * 60);
  const s  = Math.floor(((ist_h - h) * 60 - m) * 60);
  return `${String(h).padStart(2,'0')}:${String(m).padStart(2,'0')}:${String(s).padStart(2,'0')}`;
}

