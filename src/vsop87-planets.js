// রাহুর গড় গতি (Mean Lunar Node) - Meeus সূত্র
function meanRahuLongitude(jd) {
    const T = (jd - 2451545.0) / 36525.0;
    
    // উন্নত বহুপদী সূত্র (JPL-অনুমোদিত)
    const meanNode = 125.0445479 
                   - 1934.1362608 * T 
                   + 0.0020754 * T * T 
                   + 0.00000214 * T * T * T 
                   - 0.000000038 * T * T * T * T;
    
    return rev(meanNode);
}

// গ্রহীয় বিচলন গণনা (Planetary Perturbations on the Lunar Node)
function computeRahuPerturbations(jd) {
    const T = (jd - 2451545.0) / 36525.0;
    
    // মৌলিক চান্দ্র যুক্তিসমূহ (Fundamental Lunar Arguments)
    const D = rev(297.8501921 + 445267.1114034 * T - 0.0018819 * T * T) * RAD;
    const M = rev(134.9633964 + 477198.8675055 * T + 0.0087414 * T * T) * RAD;
    const Ms = rev(357.5291092 + 35999.0502909 * T - 0.0001537 * T * T) * RAD;
    const F = rev(93.2720950 + 483202.0175233 * T - 0.0036539 * T * T) * RAD;
    
    // গ্রহগুলোর গড় দ্রাঘিমাংশ (Mean Longitudes for Planetary Terms)
    const Ve = rev(181.9798 + 58519.2130 * T) * RAD;   // শুক্র
    const Ju = rev(34.3515 + 3034.9057 * T) * RAD;     // বৃহস্পতি
    const Ma = rev(355.4533 + 19140.3027 * T) * RAD;   // মঙ্গল
    const Sa = rev(50.0774 + 1222.1138 * T) * RAD;     // শনি
    
    let perturbation = 0.0;
    
    // প্রধান চান্দ্র বিচলন টার্ম গণনা
    RAHU_PERTURBATION_TERMS.forEach(term => {
        const ampl = term[0];           // প্রশস্ততা (আর্কসেকেন্ড)
        const iD = term[1];
        const iM = term[2];
        const iMs = term[3];
        const iF = term[4];
        const isSine = term[5];
        
        // কোন যুক্তি = iD*D + iM*M + iMs*Ms + iF*F
        const arg = iD * D + iM * M + iMs * Ms + iF * F;
        
        if (isSine === 1) {
            perturbation += ampl * Math.sin(arg);
        } else {
            perturbation += ampl * Math.cos(arg);
        }
    });
    
    // অতিরিক্ত গ্রহীয় প্রভাব
    perturbation += 0.0082 * Math.sin(Ve - 2*F);
    perturbation -= 0.0056 * Math.sin(2*F - Ve);
    perturbation += 0.0034 * Math.sin(2*Ju - 3*F);
    perturbation -= 0.0028 * Math.sin(Ma - F);
    perturbation += 0.0019 * Math.sin(Sa - 2*F);
    
    // দীর্ঘ-পর্যায়ের অতি সূক্ষ্ম সংশোধনী
    const omega = rev(125.04452 - 1934.136261 * T) * RAD;
    perturbation -= 0.0012 * Math.sin(omega);
    perturbation += 0.0008 * Math.sin(2 * omega);
    
    // আর্কসেকেন্ড থেকে ডিগ্রিতে রূপান্তর
    return perturbation / 3600.0;
}

// ────────────────────────────────────────────────────────────────────────────
// রাহুর সায়ন (Tropical) দ্রাঘিমাংশ — High Precision with Perturbations
// ────────────────────────────────────────────────────────────────────────────
function rahuL(jd) {
    // 1. রাহুর গড় দ্রাঘিমাংশ
    const meanLon = meanRahuLongitude(jd);
    
    // 2. গ্রহীয় বিচলন যোগ
    const perturbation = computeRahuPerturbations(jd);
    
    // 3. চূড়ান্ত দ্রাঘিমাংশ
    return rev(meanLon + perturbation);
}

