// ────────────────────────────────────────────────────────────────────────────
function mercuryL_tropical(jd) {
    const T = (jd - 2451545.0) / 36525.0;

    // 1. বুধের হেলিওসেন্ট্রিক অবস্থান — L (দ্রাঘিমাংশ), B (অক্ষাংশ), R (ব্যাসার্ধ)
    const mercHelioLon = computeMercuryHeliocentricL(T) * DEG;
    const mercHelioB   = computeMercuryHeliocentricB(T); // radians — বুধের ৭° নতি
    const mercHelioR   = computeMercuryHeliocentricR(T);

    // 2. পৃথিবীর হেলিওসেন্ট্রিক অবস্থান
    const earth = _earthHel(jd);

    // 3. ৩D জিওসেন্ট্রিক দ্রাঘিমাংশ — B উপেক্ষা করলে ~৯' ভুল হয়
    const geo = _geo3D(mercHelioLon, mercHelioB, mercHelioR, earth.lon, earth.r);

    return rev(geo);
}

