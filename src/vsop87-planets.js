// VSOP87D সিরিজ গণনার মূল ফাংশন (T = জুলিয়ান শতাব্দী J2000 থেকে)
// সহগগুলো 10^-8 radians একক — সঠিক রূপান্তর: / 1e8 * (180/π)
function computeVSOP87Longitude(T) {
    const tau = T / 10; // Julian centuries → Julian millennia (VSOP87 চলক)
    let L = 0.0;
    VSOP87_SUN_TERMS.L0.forEach(term => {
        L += term[0] * Math.cos(term[1] + term[2] * tau);
    });
    VSOP87_SUN_TERMS.L1.forEach(term => {
        L += term[0] * Math.cos(term[1] + term[2] * tau) * tau;
    });
    VSOP87_SUN_TERMS.L2.forEach(term => {
        L += term[0] * Math.cos(term[1] + term[2] * tau) * tau * tau;
    });
    return L / 1e8 * DEG; // 10^-8 radian → degree
}

