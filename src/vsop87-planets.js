// সূর্যের উন্নত অবস্থান (VSOP87D ভিত্তিক High-Precision সিরিজ)
// মোট ৮৭টি পদ ব্যবহার করা হয়েছে (L0, L1, L2, L3)
// নির্ভুলতা: +/- ১ আর্কসেকেন্ডের মধ্যে (J2000 এর ৬০০০ বছরের জন্য বৈধ)
// ────────────────────────────────────────────────────────────────────────────

// ─────────────────────────────────────────────────────────────────
// কেপলারিয়ান দূরত্ব গণনা — প্রতিটি গ্রহের জন্য
// R = a(1-e²)/(1+e·cos(ν)), ν = true anomaly = L_helio - ω
// T: Julian centuries from J2000; L_helio: heliocentric longitude (degrees)
// ─────────────────────────────────────────────────────────────────
function _keplerR(L_deg, a, e, omega_deg) {
    const nu = (L_deg - omega_deg) * RAD;
    return a * (1 - e*e) / (1 + e * Math.cos(nu));
}

// কেপলার সত্য অ্যানোমালি থেকে পরিমার্জিত R (Newton iteration)
function _keplerR_accurate(L_deg, a, e0, dedt, omega0, domegadt, T) {
    const e     = e0 + dedt * T;
    const omega = rev(omega0 + domegadt * T);
    return _keplerR(rev(L_deg), a, e, omega);
}


