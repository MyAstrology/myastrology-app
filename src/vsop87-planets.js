// পঞ্চাঙ্গ গণনা — তিথি, নক্ষত্র, যোগ, করণ, লগ্ন, জন্মরাশি, মৃত্যুদোষ
// Bishuddha Siddhanta অনুযায়ী
// ════════════════════════════════════════════════════════════════════════════

// তিথি (Tithi): চন্দ্র-সূর্যের অন্তর প্রতি ১২° = ১ তিথি
function getTithi(jd) {
  const ay   = lahiriAY(jd);
  const sunS = rev(sunL(jd)  - ay);
  const monS = rev(moonL(jd) - ay);
  const diff = rev(monS - sunS);
  const idx  = Math.floor(diff / 12);  // 0-29
  const frac = (diff % 12) / 12;       // ০-১ (তিথির মধ্যে কতটুকু বাকি)
  return { index: idx, fraction: frac, degrees: diff };
}

// নক্ষত্র (Nakshatra): চন্দ্রের নিরয়ণ অবস্থান প্রতি ১৩°২০' = ১ নক্ষত্র
function getNakshatra(jd) {
  const moonSid = rev(moonL(jd) - lahiriAY(jd));
  const idx  = Math.floor(moonSid / (360/27));  // 0-26
  const frac = (moonSid % (360/27)) / (360/27);
  const pada = Math.floor(frac * 4) + 1;        // 1-4
  return { index: idx, pada, fraction: frac, degrees: moonSid };
}

// যোগ (Yoga): সূর্য+চন্দ্র অন্তর প্রতি ১৩°২০' = ১ যোগ
function getYoga(jd) {
  const ay   = lahiriAY(jd);
  const sum  = rev(rev(sunL(jd) - ay) + rev(moonL(jd) - ay));
  const idx  = Math.floor(sum / (360/27));
  const frac = (sum % (360/27)) / (360/27);
  return { index: idx, fraction: frac };
}

// করণ (Karana): তিথির প্রতিটি অর্ধ = ১ করণ
// panjika.html-এর KARANS/_karanaName-এর সাথে অভিন্ন (০-৫৯ চক্র):
// k=0 → কিংস্তুঘ্ন, k=1-56 → বব..বিষ্টি (৮ বার আবর্তিত), k=57-59 → শকুনি,চতুষ্পাদ,নাগ
const _KARANA_NAMES = [
  'বব','বালব','কৌলব','তৈতিল','গর','বণিজ','বিষ্টি',
  'শকুনি','চতুষ্পাদ','নাগ','কিংস্তুঘ্ন'
];
function getKarana(jd) {
  const { degrees } = getTithi(jd);
  const halfIdx = Math.floor(degrees / 6) % 60;  // প্রতি ৬° = ১ করণ
  let idx;
  if (halfIdx === 0) idx = 10; // কিংস্তুঘ্ন (স্থির)
  else if (halfIdx === 57) idx = 7; // শকুনি
  else if (halfIdx === 58) idx = 8; // চতুষ্পাদ
  else if (halfIdx === 59) idx = 9; // নাগ
  else idx = (halfIdx - 1) % 7; // বব..বিষ্টি
  return { index: idx, name: _KARANA_NAMES[idx] };
}

// মৃত্যুদোষ (Mrityu Dosha) — Vishghati: নির্দিষ্ট নক্ষত্র-তিথি-বার সংমিশ্রণ
// বিশুদ্ধ সিদ্ধান্ত পঞ্জিকা অনুযায়ী মূল ৬টি মৃত্যুদোষ
function getMrityuDosha(jd, weekday) {
  // weekday: 0=রবি, 1=সোম, ..., 6=শনি
  const { index: nak } = getNakshatra(jd);
  const { index: tit } = getTithi(jd);
  const tithi1 = tit + 1; // 1-30

  // মৃত্যুদোষ নির্দিষ্ট নক্ষত্র-পদ-তিথি-বার সংমিশ্রণ (বিশুদ্ধ সিদ্ধান্ত সারণি)
  const dosha_map = [
    // [বার, তিথি_তালিকা, নক্ষত্র_তালিকা]
    [0, [6,8,14],   [6,19,22]],   // রবিবার
    [1, [2,7,12],   [0,10,23]],   // সোমবার
    [2, [4,9,14],   [1,12,25]],   // মঙ্গলবার
    [3, [5,10,15],  [3,8,15]],    // বুধবার
    [4, [1,6,11],   [5,14,17]],   // বৃহস্পতিবার
    [5, [3,8,13],   [7,16,24]],   // শুক্রবার
    [6, [4,9,14],   [2,11,26]],   // শনিবার
  ];

  let found = false;
  for (const [day, tithis, naks] of dosha_map) {
    if (day === weekday && tithis.includes(tithi1) && naks.includes(nak)) {
      found = true; break;
    }
  }

  // অষ্টমী-অমাবস্যা-পূর্ণিমায় বিশেষ সতর্কতা
  const special = [7, 14, 15, 29, 30].includes(tithi1);

  return { dosha: found, specialAlert: special, tithiNo: tithi1, nakIdx: nak, day: weekday };
}

// লগ্ন পরিবর্তনের সময়সূচী (প্রতিটি লগ্ন সীমানার JD)
function getLagnaSlots(jd_start, jd_end, lat, lon, stepMinutes=2) {
  const step  = stepMinutes / (24*60);
  const slots = [];
  let prevLag = Math.floor(getLagna(jd_start, lat, lon) / 30);
  let slotStart = jd_start;

  for (let jd = jd_start + step; jd <= jd_end + step; jd += step) {
    const lag = Math.floor(getLagna(jd, lat, lon) / 30);
    if (lag !== prevLag) {
      slots.push({ rashi: prevLag, from: slotStart, to: jd - step });
      slotStart = jd - step;
      prevLag   = lag;
    }
  }
  slots.push({ rashi: prevLag, from: slotStart, to: jd_end });
  return slots;
}

// তিথি পরিবর্তনের সময়সূচী
function getTithiSlots(jd_start, jd_end, stepMinutes=5) {
  const step  = stepMinutes / (24*60);
  const slots = [];
  let prev  = getTithi(jd_start).index;
  let slotS = jd_start;

  for (let jd = jd_start + step; jd <= jd_end + step; jd += step) {
    const cur = getTithi(jd).index;
    if (cur !== prev) {
      slots.push({ index: prev, from: slotS, to: jd - step });
      slotS = jd - step; prev = cur;
    }
  }
  slots.push({ index: prev, from: slotS, to: jd_end });
  return slots;
}

// নক্ষত্র পরিবর্তনের সময়সূচী
function getNakshatraSlots(jd_start, jd_end, stepMinutes=5) {
  const step  = stepMinutes / (24*60);
  const slots = [];
  let prev  = getNakshatra(jd_start).index;
  let slotS = jd_start;

  for (let jd = jd_start + step; jd <= jd_end + step; jd += step) {
    const cur = getNakshatra(jd).index;
    if (cur !== prev) {
      slots.push({ index: prev, from: slotS, to: jd - step });
      slotS = jd - step; prev = cur;
    }
  }
  slots.push({ index: prev, from: slotS, to: jd_end });
  return slots;
}

// যোগ পরিবর্তনের সময়সূচী
function getYogaSlots(jd_start, jd_end, stepMinutes=5) {
  const step  = stepMinutes / (24*60);
  const slots = [];
  let prev  = getYoga(jd_start).index;
  let slotS = jd_start;

  for (let jd = jd_start + step; jd <= jd_end + step; jd += step) {
    const cur = getYoga(jd).index;
    if (cur !== prev) {
      slots.push({ index: prev, from: slotS, to: jd - step });
      slotS = jd - step; prev = cur;
    }
  }
  slots.push({ index: prev, from: slotS, to: jd_end });
  return slots;
}

// জন্মরাশি: চন্দ্রের নিরয়ণ রাশি
function getJanmaRashi(jd) {
  return Math.floor(rev(moonL(jd) - lahiriAY(jd)) / 30);
}

// সম্পূর্ণ দৈনিক পঞ্চাঙ্গ — একটি কলে সব তথ্য
function getDailyPanchang(y, m, d, lat, lon) {
  const jd  = JD(y, m, d) + 0.5 - lon/360.0 + 5.5/24.0; // IST noon
  const jd0 = JD(y, m, d) - 0.5; // previous midnight UTC

  const ss   = getSunriseSunset(y, m, d, lat, lon);
  const riseJD = ss ? ss.rise : null;
  const setJD  = ss ? ss.set  : null;

  // পঞ্চাঙ্গের মান সূর্যোদয়ের সময়ে গণনা করা হয়
  const refJD = riseJD || jd;

  const tit  = getTithi(refJD);
  const nak  = getNakshatra(refJD);
  const yog  = getYoga(refJD);
  const kar  = getKarana(refJD);
  const ay   = lahiriAY(refJD);
  const pos  = planetaryPositions(refJD);
  const lag  = getLagna(refJD, lat, lon);
  const lagR = Math.floor(lag / 30);

  // সপ্তাহের দিন
  const weekday = Math.floor((JD(y,m,d) + 1.5) % 7); // 0=রবি

  // মৃত্যুদোষ
  const md = getMrityuDosha(refJD, weekday);

  // সূর্যোদয় থেকে সূর্যাস্ত পর্যন্ত স্লট
  const dayStart = riseJD || (JD(y,m,d) + 5.5/24 - 0.5);
  const dayEnd   = setJD  || (dayStart + 12/24);

  const lagSlots = getLagnaSlots(dayStart, dayEnd, lat, lon);
  const titSlots = getTithiSlots(dayStart, dayEnd);
  const nakSlots = getNakshatraSlots(dayStart, dayEnd);
  const yogSlots = getYogaSlots(dayStart, dayEnd);

  return {
    date: { y, m, d, weekday },
    ayanamsa: ay,
    sunrise:  ss ? jdToIST(ss.rise)    : null,
    sunset:   ss ? jdToIST(ss.set)     : null,
    transit:  ss ? jdToIST(ss.transit) : null,
    tithi:    { ...tit,  slots: titSlots },
    nakshatra:{ ...nak,  slots: nakSlots },
    yoga:     { ...yog,  slots: yogSlots },
    karana:   kar,
    lagna:    { degrees: lag, rashi: lagR, slots: lagSlots },
    janmaRashi: getJanmaRashi(refJD),
    mrityuDosha: md,
    planets:  pos,
  };
}

