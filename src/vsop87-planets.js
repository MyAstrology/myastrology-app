const PI  = Math.PI;
const RAD = PI / 180;
const DEG = 180 / PI;
const rev = x => ((x % 360) + 360) % 360;

// ─────────────────────────────────────────────
// JULIAN DATE
// ─────────────────────────────────────────────
function JD(y, m, d) {
  if (m <= 2) { y--; m += 12; }
  const A = Math.floor(y / 100), B = 2 - A + Math.floor(A / 4);
  return Math.floor(365.25 * (y + 4716)) + Math.floor(30.6001 * (m + 1)) + d + B - 1524.5;
}

