const PADA_NAMES = ['প্রথম','দ্বিতীয়','তৃতীয়','চতুর্থ'];

const DEF_LAT = 22.5726;
const DEF_LON = 88.3639;

export function getNamakaranResult(dateStr, timeStr = '12:00', lat = DEF_LAT, lon = DEF_LON) {
  const [y, mo, d]  = dateStr.split('-').map(Number);
  const [h, m]      = timeStr.split(':').map(Number);
  const jd          = v.JD_IST(y, mo, d, h + (m || 0) / 60);
  const nak         = v.getNakshatra(jd);
  const nakIdx      = nak.index;
  const pada        = nak.pada;
  const akshar      = (NAK_AKSHAR[nakIdx] || [])[pada - 1] || '—';
  const info        = NAK_INFO[nakIdx] || {};

  return {
    nakshatra:  NAK_NAMES[nakIdx] || '—',
    nakshatraIdx: nakIdx,
    pada,
    padaName:   PADA_NAMES[pada - 1] || '',
    akshar,
    // সমস্ত অক্ষর
    allAkshar:  NAK_AKSHAR[nakIdx] || [],
    // নক্ষত্র বিবরণ
    lord:       info.lord  || '—',
    gana:       info.gana  || '—',
    yoni:       info.yoni  || '—',
    nadi:       info.nadi  || '—',
    varna:      info.varna || '—',
  };
}
